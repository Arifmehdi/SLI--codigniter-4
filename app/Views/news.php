              <p>In a move that will impact tens of thousands of owner-operators in California, the U.S. 9th Circuit upheld the state’s Assembly Bill 5’s ABC test for classifying truckers as either employees or independent contractors, overturning a previously granted injunction. The ruling was 2-1.

The International Brotherhood of Teamsters served as the intervenor-defendant.

The injunction was filed by the California Trucking Association, arguing the ABC test used to define who is a gig worker, was contrary to the precedent set by the Federal Aviation Administration Authorization Act of 1994 (F4A).</p>

              <p>“AB 5 threatens the livelihood of more than 70,000 independent truckers,” said CTA ‘s CEO Shawn Yadon in 2019, when the CTA and two owner-operators filed for the injunction. “The bill wrongfully restricts their ability to provide services as owner-operators and, therefore, runs afoul of federal law.”</p>

              <p>In the opinion written by 9th Circuit Judge Sandra Ikuta, the court found: “Because AB-5 is a generally applicable labor law that affects a motor carrier’s relationship with its workforce and does not bind, compel, or otherwise freeze into place the prices, routes, or services of motor carriers, we conclude that it is not preempted by the F4A.” </p>

